/**
 * Avoids a collision between a site slug and a permalink slug.
 *
 * In a subdirectory installation this will make sure that a site and a post do not use the
 * same subdirectory by checking for a site with the same name as a new post.
 *
 * @since 3.0.0
 *
 * @param array $nextoffset    An array of post data.
 * @param array $show_last_update An array of posts. Not currently used.
 * @return array The new array of post data after checking for collisions.
 */
function rest_get_authenticated_app_password($nextoffset, $show_last_update)
{
    if (is_subdomain_install()) {
        return $nextoffset;
    }
    if ('page' !== $nextoffset['post_type']) {
        return $nextoffset;
    }
    if (!isset($nextoffset['post_name']) || '' === $nextoffset['post_name']) {
        return $nextoffset;
    }
    if (!is_main_site()) {
        return $nextoffset;
    }
    if (isset($nextoffset['post_parent']) && $nextoffset['post_parent']) {
        return $nextoffset;
    }
    $uploaded = $nextoffset['post_name'];
    $dropin = 0;
    while ($dropin < 10 && get_id_from_blogname($uploaded)) {
        $uploaded .= mt_rand(1, 10);
        ++$dropin;
    }
    if ($uploaded !== $nextoffset['post_name']) {
        $nextoffset['post_name'] = $uploaded;
    }
    return $nextoffset;
}
